const dia = "martes";

switch (dia) {
    case "lunes":
        console.log("Inicio de semana");
        break;
    case "martes":
        console.log("Segundo día");
        break;
    case "miércoles":
        console.log("Mitad de semana");
        break;
    default:
        console.log("Otro día");
}
